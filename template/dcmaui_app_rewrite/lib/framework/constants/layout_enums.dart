/// Flutter-friendly layout enums that map to Yoga enums

/// Direction of layout
enum Direction {
  ltr,
  rtl,
}

/// Flex direction
enum FlexDirection {
  column,
  columnReverse,
  row,
  rowReverse,
}

/// Justify content
enum JustifyContent {
  flexStart,
  center,
  flexEnd,
  spaceBetween,
  spaceAround,
  spaceEvenly,
}

/// Align items
enum AlignItems {
  auto,
  flexStart,
  center,
  flexEnd,
  stretch,
  baseline,
  spaceBetween,
  spaceAround,
}

/// Align self
enum AlignSelf {
  auto,
  flexStart,
  center,
  flexEnd,
  stretch,
  baseline,
  spaceBetween,
  spaceAround,
}

/// Align content
enum AlignContent {
  flexStart,
  center,
  flexEnd,
  stretch,
  spaceBetween,
  spaceAround,
}

/// Flex wrap
enum FlexWrap {
  nowrap,
  wrap,
  wrapReverse,
}

/// Position type
enum PositionType {
  relative,
  absolute,
}

/// Display type
enum Display {
  flex,
  none,
}

/// Overflow behavior
enum Overflow {
  visible,
  hidden,
  scroll,
}
