/// Direction of layout
enum YogaDirection {
  ltr,
  rtl,
}

/// Flex direction
enum YogaFlexDirection {
  column,
  columnReverse,
  row,
  rowReverse,
}

/// Justify content
enum YogaJustifyContent {
  flexStart,
  center,
  flexEnd,
  spaceBetween,
  spaceAround,
  spaceEvenly,
}

/// Align items
enum YogaAlign {
  auto,
  flexStart,
  center,
  flexEnd,
  stretch,
  baseline,
  spaceBetween,
  spaceAround,
}

/// Flex wrap
enum YogaWrap {
  nowrap,
  wrap,
  wrapReverse,
}

/// Edge for margin/padding
enum YogaEdge {
  left,
  top,
  right,
  bottom,
  start,
  end,
  horizontal,
  vertical,
  all,
}

/// Position type
enum YogaPositionType {
  relative,
  absolute,
}

/// Layout direction for display
enum YogaDisplay {
  flex,
  none,
}

/// Unit for layout properties
enum YogaUnit {
  undefined,
  point,
  percent,
  auto,
}

/// Overflow behavior
enum YogaOverflow {
  visible,
  hidden,
  scroll,
}
